package JavaClasses;

public class Modifiers {
    // Modifiers are keywords that you can use to change the way a class, method, or variable behaves.
    // They can be used to control access, inheritance, and other aspects of the code.

    // Access Modifiers:
    // 1. public: The member is accessible from any other class.
    // 2. private: The member is accessible only within its own class.
    // 3. protected: The member is accessible within its own package and by subclasses.
    // 4. default (no modifier): The member is accessible only within its own package.

    // Non-Access Modifiers:
    // 1. static: The member belongs to the class rather than to any specific instance.
    // 2. final: The member cannot be changed after it has been initialized.
    // 3. abstract: The class or method is declared but not implemented.
}
