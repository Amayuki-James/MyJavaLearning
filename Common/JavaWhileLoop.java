package Common;

public class JavaWhileLoop {
    public static void main(String[] args){
        //While loop
        int i = 0;
        while ( i < 5 ) {
            System.out.println("Value of i: " + i);
            i++;
        }
        //Do while Loop
        int j = 0;
        do {
            System.out.println(i);
            j++;
        }
        while ( j < 5 );
    }
    
}
