package Common;

public class DataTypes {
    byte a = 10; // 1 byte, range: -128 to 127
    short b = 20; // 2 bytes, range: -32,768 to 32,767
    int c = 30; // 4 bytes, range: -2,147,483,648 to 2,147,483,647
    long d = 40L; // 8 bytes, range: -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
    float e = 50.5f; // 4 bytes, sufficient for 7 decimal digits
    double f = 60.5; // 8 bytes, sufficient for 15 decimal digits
    char g = 'A'; // 2 bytes, Unicode characters or ASCII characters
    boolean h = true; // 1 bit, true or false
}
